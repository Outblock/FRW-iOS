    case switchEthereumChain // legacy compatible
    case switchChain
}

// MARK: - RLPEncodable

protocol RLPEncodable {
    var rlpList: [Any] { get }
}

extension RLPEncodable {
    var rlpList: [Any] {
        let mirror = Mirror(reflecting: self)
        return mirror.children.compactMap { $0.value }
    }
}

